import "./Row.css";

import React from "react";

import assert from "assert";
import lodash from "lodash";


////////////////////////////////////////////////////////////////////////////////
//
//  Bracket row view class.
//
////////////////////////////////////////////////////////////////////////////////

class Row extends React.Component
{
  constructor ( props )
  {
    // Call the base class's constructor.
    super ( props );
  }

  render()
  {
    return this._render();
  }
}


////////////////////////////////////////////////////////////////////////////////
//
//  Render.
//
////////////////////////////////////////////////////////////////////////////////

Row.prototype._render = function()
{
  const props = this.props;
  const column = props.column;
  const row = props.row;
  const width = props.width;
  const height = props.height;

  assert.ok ( lodash.isSafeInteger ( column ) );
  assert.ok ( lodash.isSafeInteger ( row ) );
  assert.ok ( lodash.isSafeInteger ( width ) && width > 0 );
  assert.ok ( lodash.isSafeInteger ( height ) && height > 0 );

  const style = {
    width: width,
    height: height
  };

  return (
    <div
      className = "bracket_row"
      style = { style }
    >
      C{ column } R{ row }
    </div>
  );
};


////////////////////////////////////////////////////////////////////////////////
//
//  The end of the module.
//
////////////////////////////////////////////////////////////////////////////////

export default Row;
